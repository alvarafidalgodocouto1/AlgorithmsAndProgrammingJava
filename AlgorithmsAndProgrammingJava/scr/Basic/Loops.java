public class Loops {
    public static void main(String[] args) {

        // Repetition (The numbers are defined beforehand): for // Wiederholung: for

        // Print numbers from 1 to 100 // Zahlen von 1 bis 100 ausgeben

        // Option 1: declarar dentro do for
        for (int i = 1; i <= 100; i++) {
            System.out.println("Number: " + i);
        }
    }
}
